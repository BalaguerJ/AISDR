"""
campaigns.py — Agent 2: The Campaign Manager

This module handles the lifecycle of an outreach campaign:
1. Loading lists from the Prospector results folder.
2. Generating draft emails for everyone on the list (via outreach_ai).
3. Saving and updating the campaign state inside `backend/campaigns/`.
"""

import os
